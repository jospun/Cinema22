    private void ButtonSignInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonSignInMouseClicked
        
        try{
            
        if(nameInput1.getText().isEmpty() || passInput.getText().isEmpty() ){
            throw new EmptyInputException();
        }else{
            String username = nameInput1.getText();
            String password = new String(passInput.getText());
            System.out.println(password);

            
                if (uC.loginUser(username, password)){
                    JOptionPane.showMessageDialog(this, "Login successful!");

                    if(username.equalsIgnoreCase("Admin")){
                        BerandaAdmin berandaAdmin = new BerandaAdmin();
                        this.dispose();
                        berandaAdmin.setVisible(true);
                    }else{
                        BerandaStaff berandaStaff = new BerandaStaff();
                        this.dispose();
                        berandaStaff.setVisible(true);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Invalid username or password!");
                }    
        }
        
            }catch (SQLException ex) {
                Logger.getLogger(Login11.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Login11.class.getName()).log(Level.SEVERE, null, ex);
            } catch (EmptyInputException e){
                e.showMessage();
        }
    }//GEN-LAST:event_ButtonSignInMouseClicked

